            <h2 data-aos="flip-left"> Teatro Sala </h2>
            <p>El teatro ​ es la rama de las artes escénicas relacionada con la actuación. Representa
               historias actuadas frente a los espectadores o frente a una cámara usando una combinación de discurso,
                gestos, escenografía, música, sonido o espectáculo.

              También se entiende como «teatro» el género literario que comprende las obras de teatro representadas ante un público
              o para ser grabadas y reproducidas en el cine, así como a la edificación donde se presentan tradicionalmente dichas obras o 
              grabaciones. En adición a la narrativa común, el estilo de diálogo, el teatro también toma otras formas como la ópera, el ballet, el cine, la ópera china o la pantomima.
