            <h2 data-aos="flip-left"> Circo</h2>
            <p> Los espectáculos de circo contemporáneo vienen apoderándose de Colombia de un tiempo para acá.
               Varias compañías circenses visitan el país casi mensualmente y enamoran a niños y a grandes en casa
                presentación. Pero, ¿qué es un circo contemporáneo?
                Son actos que combinan las artes circenses tradicionales y técnicas teatrales para transmitir una historia o tema.  
                El show se basa en las acrobacias de los artistas, complementado música interpretada en vivo, así como por la impresionante e
                scenografía y efectos de luces.
                Hablar de dramaturgia en Circo es hablar de un proceso de reinterpretación y reflexión de una dramaturgia con reglas estrictas e
                 inapelables como es el circo tradicional, hacia una hibridación entre las diversas artes escénicas para crear una nueva estructura
                con elementos integradores como lo es el Circo Contemporáneo, considerando también los elementos históricos y culturales que entregan 
                una variedad de expresiones circenses únicas y extraordinarias.
            </p>
